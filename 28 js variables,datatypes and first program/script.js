//Variables

// In simple terms a variable in js is like a ontainer that holds information
// That information can be a number,text,other data .

// example 
var a = 1;
var b = 'sairam';
var c = "123hello";

// Console.log is used to print the statements

//printing output

console.log(a);
console.log(b);
console.log(c);

// but instead of var we use let for efficient use of variables
// because var is global scoped and let is block scoped 

// so the above example can be written as

let d = 1;
let e ='sairam';
let f = "hello123";

console.log(d);
console.log(e);
console.log(f);

