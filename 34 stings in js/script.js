// Strings in js

// strings in js are like digital pieces of text that you can use to display words , sentences , or any kind of textual information

// creating strings :- you can create a string by enclosing your text in single quotes ('')  or double quotes(" ")

let str = "My name is SaiRam";
console.log(str);


// Combining two strings :- you can combine strings using the '+' operator.

let a = "Sai";
let b = "Ram";
let c = a+b;
console.log(c);

// to get space between two strings

let d = a +' '+b;
console.log(d);

