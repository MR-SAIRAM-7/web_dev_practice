// post increment 

let num1 = 1;
console.log("The value of num1 is " + (num1++));

// post increment increases by the value 1 but at first iteration it prints the original value and then increases by 1

let num2 = 1;
console.log("The value of num1 is " + (++num2));

// pre increment increases by the value 1  at first iteration 

let num3 = 1;
console.log("The value of num1 is " + (num1--));

// post decrement decreases by the value 1 but at first iteration it prints the original value and then decreases by 1

let num4 = 1;
console.log("The value of num1 is " + (--num1));

// pre  decrement decreases by the value 1  at first iteration 

